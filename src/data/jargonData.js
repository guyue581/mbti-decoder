// 随机获取一条黑话
export function getRandomJargon() {
  return officeJargonData[Math.floor(Math.random() * officeJargonData.length)]
}

// 按类别获取
export function getJargonByCategory(category) {
  return officeJargonData.filter(j => j.category === category)
}

// 获取所有类别
export function getCategories() {
  return [...new Set(officeJargonData.map(j => j.category))]
}

// 搜索黑话
export function searchJargon(keyword) {
  if (!keyword) return officeJargonData
  const lower = keyword.toLowerCase()
  return officeJargonData.filter(j => 
    j.phrase.toLowerCase().includes(lower) || 
    j.meaning.toLowerCase().includes(lower)
  )
}
